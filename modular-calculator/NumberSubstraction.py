"""
Subtraction Module
This module provides a function for performing subtraction.
"""

# Function Name: PerformSubtraction
# Description: Subtracts second number from the first number.
# Parameters:
#     a (float) - First number
#     b (float) - Second number
# Return:
#     float - Result of subtraction
def PerformSubtraction(a, b):
    return a - b
